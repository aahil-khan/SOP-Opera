"""
Independent ground truth for the eval harness.

## Why this module exists

The previous labels were generated by `_dangerous_at_step()`, whose docstring
read "When compound fusion should reach blocking before single-sensor critical."
That defined truth as *whatever our detector does*, so the compound engine scored
a 0% false-negative rate by construction. The number was unfalsifiable.

Ground truth here is instead derived from **statutory stop-work criteria**: given
a plant state, would an applicable safety provision require work to stop? That
question has an answer independent of whether we detect it.

## Independence rules (enforced by tests/test_eval_independence.py)

1. This module must not import `app.risk.policy`, and must not call
   `evaluate_rules` or any `rule_*` function. It reads **raw context payloads**,
   not our derived facts.
2. The criteria below are phrased the way the regulation is phrased — around
   permitted activity and exposure — deliberately *not* around the hazard
   dimensions our policy fuses. Disagreement between this module and
   `app.risk.policy` is expected and is the entire point: it is what the
   confusion matrix measures.

`ContextEntryView` is imported only as a data container; no rule logic comes
with it.

## On thresholds

The criteria reuse the configured gas/temperature thresholds. Those are physical
action levels, not detector policy — a regulation citing an action level and a
detector alarming on the same number is agreement about chemistry, not
circularity. What differs is the *criterion built on top of them*.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import Callable

from app.context.derived_facts import ContextEntryView
from app.core.config import get_settings

HAZARDOUS_ZONE = "hazardous"
HOT_WORK = "hot_work"
CONFINED_SPACE = "confined_space"


@dataclass(frozen=True)
class PlantState:
    """Raw, rule-engine-free reading of a plant snapshot."""

    max_gas: float | None
    max_temp: float | None
    active_work_types: frozenset[str]
    hazardous_permits: int
    """Active permits for hot work or confined-space entry."""
    unisolated_hazardous_permits: int
    """Hazardous-work permits with no confirmed isolation record."""
    workers_in_hazardous_zone: int


def read_plant_state(entries: list[ContextEntryView]) -> PlantState:
    gas: list[float] = []
    temp: list[float] = []
    active_permits: list[ContextEntryView] = []
    confirmed_permit_ids: set = set()
    workers = 0

    for e in entries:
        payload = e.payload or {}
        if e.category == "sensor":
            g = payload.get("gas_reading")
            if isinstance(g, (int, float)):
                gas.append(float(g))
            t = payload.get("temp_reading")
            if isinstance(t, (int, float)):
                temp.append(float(t))
        elif e.category == "permit" and payload.get("status") == "active":
            active_permits.append(e)
        elif e.category == "isolation_status":
            if payload.get("isolation_confirmed") is True:
                confirmed_permit_ids.add(payload.get("permit_id"))
        elif e.category == "worker_location":
            if payload.get("zone") == HAZARDOUS_ZONE:
                workers += 1

    work_types = {
        str(p.payload.get("work_type"))
        for p in active_permits
        if p.payload.get("work_type")
    }
    hazardous = [
        p
        for p in active_permits
        if p.payload.get("work_type") in (HOT_WORK, CONFINED_SPACE)
    ]
    unisolated = [
        p for p in hazardous if p.payload.get("permit_id") not in confirmed_permit_ids
    ]

    return PlantState(
        max_gas=max(gas) if gas else None,
        max_temp=max(temp) if temp else None,
        active_work_types=frozenset(work_types),
        hazardous_permits=len(hazardous),
        unisolated_hazardous_permits=len(unisolated),
        workers_in_hazardous_zone=workers,
    )


@dataclass(frozen=True)
class StopWorkCriterion:
    code: str
    """The provision the criterion rests on."""
    name: str
    basis: str
    """Why the provision requires stopping, in the regulation's own terms."""
    applies: Callable[[PlantState], bool]


def _gas_at_or_above(state: PlantState, level: float) -> bool:
    return state.max_gas is not None and state.max_gas >= level


def _criteria() -> tuple[StopWorkCriterion, ...]:
    s = get_settings()
    gas_action = float(s.gas_elevated_threshold)
    gas_critical = float(s.gas_critical_threshold)
    temp_critical = float(s.temp_critical_threshold)

    return (
        StopWorkCriterion(
            code="Factories Act 1948 s.37(1)(b)",
            name="Flammable gas accumulation above the incident limit",
            basis=(
                "Where a process produces gas likely to explode on ignition, all "
                "practicable measures shall be taken to prevent explosion by removal "
                "or prevention of the accumulation of such gas."
            ),
            applies=lambda st: _gas_at_or_above(st, gas_critical),
        ),
        StopWorkCriterion(
            code="Factories Act 1948 s.37(1)(c)",
            name="Ignition source not excluded from a flammable atmosphere",
            basis=(
                "Explosion shall be prevented by exclusion or effective enclosure of "
                "all possible sources of ignition; the OISD-STD-105 hot-work permit "
                "is the control that gives this effect, and it requires confirmed "
                "isolation before and throughout the work."
            ),
            applies=lambda st: (
                HOT_WORK in st.active_work_types
                and _gas_at_or_above(st, gas_action)
                and st.unisolated_hazardous_permits > 0
            ),
        ),
        StopWorkCriterion(
            code="Factories Act 1948 s.41H",
            name="Personnel exposed to an uncontrolled hazardous atmosphere",
            basis=(
                "Where workers in a hazardous process face a likelihood of imminent "
                "danger to their lives or health, the occupier must take immediate "
                "remedial action."
            ),
            applies=lambda st: (
                st.workers_in_hazardous_zone > 0 and _gas_at_or_above(st, gas_action)
            ),
        ),
        StopWorkCriterion(
            code="OISD-STD-105",
            name="Incompatible simultaneous operations",
            basis=(
                "The work permit system issues distinct permits for hot work and for "
                "confined space entry; running both concurrently on the same equipment "
                "defeats the controls each permit establishes. Confined space entry "
                "additionally requires a competent person's certificate under "
                "Factories Act 1948 s.36(2)."
            ),
            applies=lambda st: {HOT_WORK, CONFINED_SPACE} <= st.active_work_types,
        ),
        StopWorkCriterion(
            code="Factories Act 1948 s.41H",
            name="Process temperature above the incident limit",
            basis=(
                "A runaway process temperature is an imminent danger to life or "
                "health requiring immediate remedial action."
            ),
            applies=lambda st: (
                st.max_temp is not None and st.max_temp >= temp_critical
            ),
        ),
    )


@dataclass(frozen=True)
class GroundTruth:
    dangerous: bool
    criteria: tuple[str, ...]
    """Codes of every stop-work provision that applies."""
    rationale: str


def label(entries: list[ContextEntryView]) -> GroundTruth:
    """Would a statutory provision require work to stop in this plant state?"""
    state = read_plant_state(entries)
    hits = [c for c in _criteria() if c.applies(state)]
    if not hits:
        return GroundTruth(
            dangerous=False,
            criteria=(),
            rationale="No stop-work provision applies to this plant state.",
        )
    return GroundTruth(
        dangerous=True,
        criteria=tuple(dict.fromkeys(c.code for c in hits)),
        rationale="; ".join(f"{c.code}: {c.name}" for c in hits),
    )


def criterion_codes() -> tuple[str, ...]:
    return tuple(dict.fromkeys(c.code for c in _criteria()))
